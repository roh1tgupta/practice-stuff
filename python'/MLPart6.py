#scale
#When your data has different values, and even different measurement units,
#it can be difficult to compare them. What is kilograms compared to meters?
# Or altitude compared to time?
#The answer to this problem is scaling. We can scale data into new
# values that are easier to compare.

#There are different methods for scaling data,
#  we will use a method called standardization.
# The standardization method uses this formula:
# z = (x - u) / s
# Where z is the new value, x is the original value,
# u is the mean and s is the standard deviation.

